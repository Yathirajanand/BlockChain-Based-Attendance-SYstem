package com.attendance.form;

public class StudentForm {

	private String rno;
	private String name;
	private String dob;
	private String email;
	private String pno;
	private String address;
	private String photo;
	private String regulation;
	private String yearOfJoin;
	private String currentSem;
	private String branch;
	private String parrentEmail;
	
	public String getRno() {
		return rno;
	}
	public void setRno(String rno) {
		this.rno = rno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPno() {
		return pno;
	}
	public void setPno(String pno) {
		this.pno = pno;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getRegulation() {
		return regulation;
	}
	public void setRegulation(String regulation) {
		this.regulation = regulation;
	}
	public String getYearOfJoin() {
		return yearOfJoin;
	}
	public void setYearOfJoin(String yearOfJoin) {
		this.yearOfJoin = yearOfJoin;
	}
	public String getCurrentSem() {
		return currentSem;
	}
	public void setCurrentSem(String currentSem) {
		this.currentSem = currentSem;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getParrentEmail() {
		return parrentEmail;
	}
	public void setParrentEmail(String parrentEmail) {
		this.parrentEmail = parrentEmail;
	}
	
	
}
